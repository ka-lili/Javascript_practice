// // Simulate a traffic light using a switch case. 
// The program should output the color of the traffic light based 
// on user input (1 for red, 2 for yellow, 3 for green).

let number = 3;
switch(number){
    case 1:
        console.log('Red');
        break;
    case 2:
        console.log('Yellow');
        break;
    case 3:
        console.log('green');
        break;
    default:
        console.log('none');            
}
